/* 기타 연산자 : 관계 연산자 ==, !=, <, <=, >, >= */

package step03;

public class Test03_2 {

  public static void main(String[] args) {
    System.out.println(5 == 3);
    System.out.println(5 != 3);
    System.out.println(5 < 3);
    System.out.println(5 <- 3);
    System.out.println(5 > 3);
    System.out.println(5 >= 3);

  }

}
